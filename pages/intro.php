	<div class="titleBar">
		<h1>What &amp; Why?</h1>
	</div>
	<p>
		<a href="http://xenforo.com/">XenForo</a> is a relatively new yet fairly mature &amp; feature rich Forum application
		by XenForo Ltd. XenForo is being developed by (currently) two developers:
		<a href="http://xenforo.com/community/members/kier.2/">Kier Darby</a> and
		<a href="http://xenforo.com/community/members/mike.3/">Mike Sullivan</a>. Underlying this forum application is a framework.
		A set of classes and objects that are designed to be extended, used and reused in your actual application.
		This framework is based on the MVC (Model-View-Controller) design pattern.
	</p>
	<p>
		What this means for a developer is that the business logic is completely separated with the presentation, making it
		easier to reuse and change something without creating any unnecessary side-effects. For an addon developer, this means
		that core functionality can be extended with very little effort.
	</p>
	<p>
		Since the first Beta release (<a href="http://xenforo.com/community/threads/xenforo-1-0-0-beta-1-released.4858/">Beta 1</a>
		on October 5), the developer forums have <a href="http://xkcd.com/725/">figuratively</a> exploded and the addons are being
		released at a very fast pace: 100+ addons &amp; template modifications in the first month itself.
	</p>
	<p>
		What's lacking (and that's completely understandable) was a development documentation or guide that would help get
		developers started with the XenForo Framework &amp; Forum application. By documentation I mean something to the tune
		of what Symfony &amp; CodeIgniter have in the form of
		<a href="http://www.symfony-project.org/gentle-introduction/1_4/en/">A Gentle Introduction to symfony</a>
		and <a href="http://codeigniter.com/user_guide/toc.html">CodeIgniter User Guide</a>.
	</p>
